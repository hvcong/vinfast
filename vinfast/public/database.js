// Helper function to get colors for a product
function getColorsForProduct(productCode) {
    const mappedCode = product_mapping[productCode] || productCode.toUpperCase();
    const productData = vinfast_colors_data[mappedCode];
    
    if (!productData) return [];
    
    return productData.colors.map(color => ({
        ma_mau: color.code,
        ten_mau: color.name || color.code,
        hinh_anh_ngoai: color.car_image_url,
        icon: color.color_icon_url || color.car_image_url
    }));
}

// Helper function to get interior colors by name
function getInteriorColorsByNames(colorNames) {
    if (!colorNames || colorNames.length === 0) return [];
    
    const nameMapping = {
        'black': 'Black',
        'granite black': 'Black',
        'saddle brown': 'Saddle Brown',
        'beige': 'Beige',
        'mocca brown': 'Mocca Brown',
        'light grey': 'Light Grey',
        'grey': 'Grey',
        'beige grey': 'Beige Grey',
        'navy blue': 'Navy Blue',
        'light beige': 'Light Beige'
    };
    
    return colorNames.map(name => {
        const normalizedName = nameMapping[name.toLowerCase()] || name;
        const color = interior_colors.find(c => c.name === normalizedName);
        if (color) {
            return {
                ma_mau_noi: color.code,
                ten_mau_noi: color.name,
                icon: color.color_icon_url
            };
        }
        return null;
    }).filter(c => c !== null);
}


